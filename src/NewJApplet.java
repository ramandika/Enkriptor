    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        final JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(jButton3);
        String temp;
        StringBuilder result=new StringBuilder();
        if(returnVal==JFileChooser.APPROVE_OPTION){
            String pathFile=fc.getSelectedFile().toString();
            jTextField2.setText(pathFile);
            try {
                BufferedReader in=new BufferedReader(new FileReader(pathFile));
                while((temp=in.readLine())!=null) result.append(temp);
                jTextArea1.setText(result.toString());
            } catch (Exception ex) {
                Logger.getLogger(NewJApplet.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTextArea2.getText().length()!=0){
            final JFileChooser fc=new JFileChooser();
            int response=fc.showSaveDialog(jButton4);
            if(response==JFileChooser.APPROVE_OPTION){
                File fileToSave = fc.getSelectedFile();
                System.out.println("Save as file: " + fileToSave.getAbsolutePath());
                BufferedWriter writer = null;
                try
                {
                    writer = new BufferedWriter( new FileWriter(fileToSave.getAbsoluteFile()));
                    writer.write(jTextArea2.getText());
                }
                catch (IOException e){
                    System.out.println("I/O problem detected");
                }
                finally
                {
                    try
                    {
                        if ( writer != null)
                        writer.close( );
                    }
                    catch ( IOException e)
                    {
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


