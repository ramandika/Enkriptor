    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            String toBeDecrypted=jTextArea2.getText();
            String keyword=jTextField1.getText();
            String decrypted;
            if(jRadioButton1.isSelected()){
                if(jCheckBox1.isSelected()) decrypted=Enkriptor.decrypt_vigenere_autokey(toBeDecrypted,keyword);
                else decrypted=Enkriptor.decrypt_vigenere(toBeDecrypted,keyword);
                jTextArea1.setText(decrypted);
            }
            else if(jRadioButton2.isSelected()){
                if(jCheckBox1.isSelected()) decrypted=Enkriptor.decrypt_vigenere_ASCII_autokey(toBeDecrypted,keyword);
                else decrypted=Enkriptor.decrypt_ASCII(toBeDecrypted,keyword);
                jTextArea1.setText(decrypted);
            }
            else if(jRadioButton3.isSelected()){
                decrypted=Enkriptor.decrypt_vigenere_ASCII_autokey(toBeDecrypted, keyword);
                jTextArea1.setText(decrypted);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

