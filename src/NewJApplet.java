    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String toBeEncrypted=jTextArea1.getText();
            String keyword=jTextField1.getText();
            String encrypted;
            StringBuilder temp=new StringBuilder(keyword);
            int length=toBeEncrypted.length()-keyword.length();

            if(jRadioButton1.isSelected()){
                if(jCheckBox1.isSelected() && length>0){ //keyword diubah menjadi autokey
                    for(int i=0;i<length;i++)
                        if(toBeEncrypted.charAt(i)>=65 && toBeEncrypted.charAt(i)<=122)temp.append(toBeEncrypted.charAt(i));
                    keyword=temp.toString();
                }    
                switch (jComboBox1.getSelectedItem().toString()) {
                    case "normal":
                        encrypted=Enkriptor.vigenere_cipher(toBeEncrypted,keyword);
                        break;
                    case "remove space":
                        encrypted=Enkriptor.vigenere_cipher_remove_space(toBeEncrypted,keyword);
                        break;
                    default:
                        //group of five
                        encrypted=Enkriptor.vigenere_cipher_group_of_five(toBeEncrypted,keyword);
                        break;
                }
                jTextArea2.setText(encrypted);
            }
            else if(jRadioButton2.isSelected()){
                if(jComboBox2.getSelectedItem().equals("remove space")){
                    StringBuilder nospace=new StringBuilder();
                    for(int i=0;i<toBeEncrypted.length();i++)
                        if(toBeEncrypted.charAt(i)!=' ') nospace.append(toBeEncrypted.charAt(i));
                    encrypted=Enkriptor.vigenere_cipher_ASCII(nospace.toString(),keyword);
                }
                else encrypted=Enkriptor.vigenere_cipher_ASCII(toBeEncrypted,keyword);
                jTextArea2.setText(encrypted);
            }
            else if(jRadioButton3.isSelected()){
                for(int j=0;j<length;j++)
                     temp.append(toBeEncrypted.charAt(j));
                keyword=temp.toString();
                encrypted=Enkriptor.vigenere_cipher_ASCII(toBeEncrypted,keyword);
                jTextArea2.setText(encrypted);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

